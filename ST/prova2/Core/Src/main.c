void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {
    if (hadc->Instance == ADC1) {
        uint32_t adc_val = HAL_ADC_GetValue(hadc);
        float voltage = (adc_val / 4095.0f) * 3.3f;

        // Esegui la logica di stato qui (eviti blocchi nell'ISR timer)
        switch (stato[0]) {
            case 'r':

                // soglia oltre 1V -> stiamo rilevando presenza da 5cm in poi
                if (voltage > 1.0f) {
                    strcpy(stato, "check");
                    HAL_GPIO_WritePin(GPIOE, LD9_Pin, GPIO_PIN_SET);

                    // invio carattere tramite uart per avviare il riconoscimento tramite fotocamera
                    HAL_UART_Transmit(&huart4, &psbuf, 1, HAL_MAX_DELAY);
                }
                break;

            case 'c':

                // se ho nuova interruzione e conversione ma sono ancora in stato check non faccio nulla (evitiamo invii multipli alla esp32)
                HAL_GPIO_WritePin(GPIOE, LD9_Pin, GPIO_PIN_RESET);
                break;

                // caso in cui lo stato risulta "aperto"
            case 'a':
                if (!attesa_reset) {
                    if (voltage > 1.0f) {

                        // mi avvicino e faccio chiudere la scatola
                        servo_set_angle(0);
                        attesa_reset = 1;  // ora aspettiamo che la tensione scenda
                    }
                } else {

                    // quando mi sono allontanato torno in stato di reset
                    if (voltage < 0.5f) {
                        attesa_reset = 0;
                        strcpy(stato, "reset");  // solo ora si può resettare
                    }
                }
                break;
                
            default:
                strcpy(stato, "reset");
                HAL_GPIO_WritePin(GPIOE, LD9_Pin, GPIO_PIN_RESET);
                break;
        }
    }
}



